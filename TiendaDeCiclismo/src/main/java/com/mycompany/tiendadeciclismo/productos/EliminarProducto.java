    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        gestorTProductosArticulos gestor = new gestorTProductosArticulos();
        Iterator<TProducto> iterador = gestor.getListaTProducto().iterator();
        
        
        while (iterador.hasNext()) {
        TProducto x = iterador.next();
        if (x.getCodigo() == this.producto.getCodigo()) {
            iterador.remove(); // Eliminar el producto de la lista
            break; 
        }
    }
        gestor.guardarTProductos();
        CRUDTproducto menu = new CRUDTproducto();
        menu.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnConfirmarActionPerformed

